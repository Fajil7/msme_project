

// start first question
let plushEle = document.getElementById("plush");
let answerEle = document.getElementById("answer");
let isShowAns = false;

function isShowAnsBtn() {
    if(isShowAns === false) {
        // Hide the answer element and change the plush text to '+'
        answerEle.classList.add("d-none");
        plushEle.textContent = '+';
        isShowAns = true;
    } else if(isShowAns === true) {
        // Show the answer element and change the plush text to '-'
        answerEle.classList.remove("d-none");
        plushEle.textContent = '-';
        isShowAns = false;
    }
};

// end first question

